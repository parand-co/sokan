package ezafekari.model;

public class EzafeKariDyereGhesmatReportModel {

    private Integer radif;
    private String shp;
    private String nameoNeshan;
    private String daraje;
    private String sumSaat;
    private String sumMablagh;

    public Integer getRadif() {
        return radif;
    }

    public void setRadif(Integer radif) {
        this.radif = radif;
    }

    public String getShp() {
        return shp;
    }

    public void setShp(String shp) {
        this.shp = shp;
    }

    public String getNameoNeshan() {
        return nameoNeshan;
    }

    public void setNameoNeshan(String nameoNeshan) {
        this.nameoNeshan = nameoNeshan;
    }

    public String getDaraje() {
        return daraje;
    }

    public void setDaraje(String daraje) {
        this.daraje = daraje;
    }

    public String getSumSaat() {
        return sumSaat;
    }

    public void setSumSaat(String sumSaat) {
        this.sumSaat = sumSaat;
    }

    public String getSumMablagh() {
        return sumMablagh;
    }

    public void setSumMablagh(String sumMablagh) {
        this.sumMablagh = sumMablagh;
    }
}
